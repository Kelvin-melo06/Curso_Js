console.log("iniciando a contagem...");

setInterval(() =>{
    for(let i = 0; i <= 10; i++){
        console.log(i);
    }
}, 2000)

// setInterval é uma função que executa um código repetidamente em um intervalo de tempo especificado, em milissegundos. No exemplo acima, o código dentro do setInterval será executado a cada 2 segundos (2000 milissegundos).